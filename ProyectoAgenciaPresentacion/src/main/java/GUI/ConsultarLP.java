    private String obtenerNombreTramite(TramiteDTO tramite) {
        if (tramite instanceof LicenciaDTO) {
            return "Licencia de Conducir";
        } else if (tramite instanceof PlacaDTO) {
            return "Placa de Vehículo";
        } else {
            return "Trámite Desconocido";
        }
    }
    
    
    
